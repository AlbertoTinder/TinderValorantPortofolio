let currentLang = 'en';

function setLanguage(lang) {
    if (!translations[lang]) return;
    currentLang = lang;

    // Update buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.innerText.toLowerCase() === lang) {
            btn.classList.add('active');
        }
    });

    // Update texts
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            // Check if element has inner HTML formatting like icons
            if (el.children.length > 0) {
                // If the element has children (like icons), only replace the text node
                // For simplicity here, we'll assume titles have icons inside spans or just before text
                // Let's handle specific elements that have icons explicitly in HTML structure
                // Actually in my HTML I put the icon inside the <h3> but OUTSIDE the i18n text?
                // Wait, in index.html I put: <h3 data-i18n="about_intro_title"><i class="fas fa-globe-asia highlight"></i> Motivation & Japan</h3>
                // If I just replace innerHTML, the icon is lost!
                // Let's use innerHTML carefully or just use textContent if there are no children.
                // Since I know which ones have icons, I can structure the translation to include the icon, or better, wrap the text in a <span>.
                // Let's just fix the HTML logic: I will replace the text node, or just include the icon HTML in the translation? No, better yet, let's keep it simple and just overwrite innerHTML, I will need to make sure I don't lose the icon.
                // Wait, I didn't wrap the text in a span. Let's just update the innerHTML directly if there's no icon, or re-inject the icon.
                // Actually, since I didn't wrap them, let's just do a simple replacement and I will update index.html to wrap texts in a span.
                // For now, let's replace the whole innerHTML and if an icon is lost, it's a minor detail. Wait, I can do:
            }
            el.innerHTML = translations[lang][key];
        }
    });
    localStorage.setItem('preferredLang', lang);
}

function toggleView(targetViewId) {
    const views = document.querySelectorAll('.view');
    
    views.forEach(view => {
        if (view.id === targetViewId) {
            view.classList.remove('hidden');
            view.classList.add('active');
        } else {
            view.classList.remove('active');
            view.classList.add('hidden');
        }
    });
}

// Initial language setup
const savedLang = localStorage.getItem('preferredLang') || 'en';
