                // Since I know which ones have icons, I can structure the translation to include the icon, or better, wrap the text in a <span>.
                // Let's just fix the HTML logic: I will replace the text node, or just include the icon HTML in the translation? No, better yet, let's keep it simple and just overwrite innerHTML, I will need to make sure I don't lose the icon.
